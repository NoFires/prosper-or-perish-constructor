def _goods_output_scales(goods: Iterable[str]) -> dict[str, SequentialScale]:
    calibration = load_scale_calibration()
    configured = calibration.get("scales", {}).get("goods_output", {}) if calibration else {}
    samples = _goods_output_samples()
    return {
        good: (
            sequential_scale_from_data(configured.get(good), FALLBACK_SEQUENTIAL_SCALE)
            if configured.get(good)
            else _goods_output_scale(samples.get(good, []))
        )
        for good in goods
    }


def _goods_output_samples() -> dict[str, list[float]]:
    try:
        import polars as pl
    except ImportError:
        return {}

    sample_frames = []
    production = ROOT / "artifacts" / "data" / "savegame" / "production_method_good_flows.parquet"
    if production.exists():
        flow = pl.read_parquet(production)
        sample_frames.append(
            flow.filter(
                (pl.col("direction") == "output")
                & (pl.col("save_side") == "supplied_Production")
                & (pl.col("nominal_amount") > 0)
            )
            .group_by(["good_id", "location_id"])
            .agg(pl.col("nominal_amount").sum().alias("output"))
        )

    rgo = ROOT / "artifacts" / "data" / "savegame" / "rgo_flows.parquet"
    if rgo.exists():
        flow = pl.read_parquet(rgo)
        sample_frames.append(
            flow.filter(
                (pl.col("direction") == "output")
                & (pl.col("save_side") == "supplied_Production")
                & (pl.col("nominal_amount") > 0)
            )
            .group_by(["good_id", "location_id"])
            .agg(pl.col("nominal_amount").sum().alias("output"))
        )

    if not sample_frames:
        return {}

    samples = pl.concat(sample_frames, how="vertical_relaxed").group_by(["good_id", "location_id"]).agg(
        pl.col("output").sum().alias("output")
    )
    return {
        row["good_id"]: row["outputs"]
        for row in samples.group_by("good_id")
        .agg(pl.col("output").sort().alias("outputs"))
        .to_dicts()
    }


def _goods_output_scale(samples: list[float]) -> SequentialScale:
    values = [value for value in samples if value > 0]
    if len(values) < 5:
        return FALLBACK_SEQUENTIAL_SCALE
    values.sort()
    raw = [
        _quantile(values, 0.20),
        _quantile(values, 0.45),
        _quantile(values, 0.70),
        _quantile(values, 0.95),
    ]
    rounded = [_round_threshold(value) for value in raw]
    minimums = [0.05, 0.10, 0.25, 1.00]
    thresholds: list[float] = []
    previous = 0.0
    for value, minimum in zip(rounded, minimums):
        threshold = max(value, minimum, previous + 0.01)
        thresholds.append(round(threshold, 2))
        previous = thresholds[-1]
    return SequentialScale(tuple(thresholds))


def _quantile(values: list[float], q: float) -> float:
    index = max(0, min(len(values) - 1, round((len(values) - 1) * q)))
    return values[index]


def _round_threshold(value: float) -> float:
    if value < 1:
        return round(value, 2)
    if value < 10:
        return round(value * 2) / 2
    return round(value)


def _script_values_file(goods: Iterable[str]) -> str:
    blocks = [
        "# Generated by tools/generate_pp_goods_output_map_modes.py - do not edit by hand.",
        "# Local monthly goods output x location market_access (cf. Location.GetMarketAccess).",
        "",
    ]
    for good in goods:
        blocks.append(_script_value_block(good))
    return "\n".join(blocks).rstrip() + "\n"


def _script_value_block(good: str) -> str:
    return f"""pp_{good}_output_raw = {{
\tvalue = "goods_output(goods:{good})"
\tmultiply = market_access
}}
"""


def _localization_file(goods: Iterable[str]) -> str:
    lines = [
        "l_english:",
        "  # Generated by tools/generate_pp_goods_output_map_modes.py - do not edit by hand.",
    ]
    for good in goods:
        upper = good.upper()
        lines.extend(
            [
                f'  mapmode_pp_{good}_output_name: "[ShowGoodsName(\'{good}\')|e] Output (x Market Access)"',
                f'  MAPMODE_PP_{upper}_OUTPUT: "{_legend_text(good)}"',
                f'  MAPMODE_PP_{upper}_OUTPUT_VERY_LOW: "Very low output"',
                f'  MAPMODE_PP_{upper}_OUTPUT_LOW: "Low output"',
                f'  MAPMODE_PP_{upper}_OUTPUT_MEDIUM: "Moderate output"',
                f'  MAPMODE_PP_{upper}_OUTPUT_HIGH: "High output"',
                f'  MAPMODE_PP_{upper}_OUTPUT_CAPPED: "Exceptional output"',
                f'  MAPMODE_PP_{upper}_OUTPUT_TT_LAND: "{_land_tooltip_text(good)}"',
                f'  MAPMODE_PP_{upper}_OUTPUT_TT_WATER: "$MAPMODE_PP_LOCAL_OUTPUT_TT_WATER$"',
            ]
        )
    return "\n".join(lines) + "\n"


def _legend_text(good: str) -> str:
    base = (
        f"#T $mapmode_pp_{good}_output_name$#!\\n"
        f"Colors [locations|e] by local [ShowGoodsName('{good}')|e] [output|e] x "
        "[market_access|e]. Red marks low output, yellow marks the middle of that good's observed output, and green marks the strongest output; zero-output locations are left without overlay."
    )
    if good == "marble":
        base += (
            " Marble comes from dedicated [ShowBuildingTypeName('marble_quarry')|e] and "
            "[ShowBuildingTypeName('marble_saw_yard')|e] production."
        )
    return base


def _land_tooltip_text(good: str) -> str:
    base = (
        f"#T $mapmode_pp_{good}_output_name$#!\\n"
        f"[ROOT.GetLocation.GetName]: [ROOT.GetLocation.MakeScope.ScriptValue('pp_{good}_output_raw')|2] = "
        f"[ShowGoodsName('{good}')|e] [output|e] x [market_access|e] (per month).\\n"
        "[market_access|e]: [ROOT.GetLocation.GetMarketAccess|2%V]."
    )
    if good == "marble":
        base += (
            "\\n#italic Marble is produced by dedicated [ShowBuildingTypeName('marble_quarry')|e] "
            "and [ShowBuildingTypeName('marble_saw_yard')|e] buildings.#!"
        )
    return base


def _ensure_icons(mod_root: Path, goods: Iterable[str], project: dict) -> None:
    vanilla_root = _vanilla_root(project)
    target = mod_root / IN_GAME_ICON_REL
    target.mkdir(parents=True, exist_ok=True)

    missing: list[str] = []
    for good in goods:
        destination = target / f"pp_{good}_output.dds"
        if destination.exists():
            continue
        source = vanilla_root / "game" / TRADE_GOOD_ICON_REL / f"icon_goods_{good}.dds"
        if source.is_file():
            shutil.copy2(source, destination)
        else:
            missing.append(good)

    if missing:
        raise FileNotFoundError("Missing icon source for goods: " + ", ".join(missing))


def _write_crlf(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8-sig", newline="\r\n") as stream:
        stream.write(text)


