def main() -> None:
    project = _load_project_config(PROJECT_CONFIG)
    mod_root = _project_path(project["project"]["mod_root"])
    goods = sorted(_all_goods(project))

    _write_crlf(mod_root / MAP_MODE_REL, _map_modes_file(goods))
    _write_crlf(mod_root / SCRIPT_VALUES_REL, _script_values_file(goods))
    _write_crlf(mod_root / LOCALIZATION_REL, _localization_file(goods))
    _ensure_icons(mod_root, goods, project)
    print(f"Generated goods output map modes for {len(goods)} goods.")


def _load_project_config(path: Path) -> dict:
    with path.open("rb") as stream:
        return tomllib.load(stream)


def _project_path(raw: str) -> Path:
    windows_path = _wsl_path_from_windows(raw)
    if windows_path is not None:
        return windows_path
    path = Path(raw)
    return path if path.is_absolute() else (ROOT / path).resolve()


def _wsl_path_from_windows(raw: str) -> Path | None:
    match = re.match(r"^([A-Za-z]):[\\/](.*)$", raw)
    if match is None:
        return None
    drive = match.group(1).lower()
    tail = match.group(2).replace("\\", "/")
    return Path("/mnt") / drive / tail


def _all_goods(project: dict) -> list[str]:
    parser = project.get("parser", {})
    profile = str(parser.get("profile") or "constructor")
    load_order = _project_path(str(parser.get("load_order") or "constructor.load_order.toml"))
    data = load_eu5_data(profile=profile, load_order_path=load_order)
    return data.goods.select("name").to_series().to_list()


def _vanilla_root(project: dict) -> Path:
    parser = project.get("parser", {})
    load_order = _project_path(str(parser.get("load_order") or "constructor.load_order.toml"))
    with load_order.open("rb") as stream:
        raw = tomllib.load(stream)
    paths = raw.get("paths", {})
    vanilla_root = _project_path(str(paths.get("vanilla_root", "")))
    if not vanilla_root.is_absolute():
        vanilla_root = (load_order.parent / vanilla_root).resolve()
    return vanilla_root


